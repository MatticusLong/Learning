# A Simple Calculator program

def add(a, b):
  answer = a + b
  print(answer)

def subtract(a, b):
  answer = a - b
  print(answer)

def multiply(a, b):
  answer = a * b
  print(answer)

def divide(a, b):
  answer = a / b
  print(answer)

def exp(a, b):
  answer = a ** b
  print(answer)


print("Exponent")
exp(4, 2)

print("Add")
add(5, 5)

print("subtract")
subtract(10, 2)

print("multiply")
multiply(2, 5)

print("divide")
divide(10, 2)


